//4) Create an Empty List of Type String Called Days, Use the Add Method to Add Names of 7 Days and Print All Days
void main() {
  // Create an empty list of type String
  List<String> days = [];

  // Add names of 7 days to the list
  days.add('Monday');
  days.add('Tuesday');
  days.add('Wednesday');
  days.add('Thursday');
  days.add('Friday');
  days.add('Saturday');
  days.add('Sunday');

  // Print all days
  print(days);
}
